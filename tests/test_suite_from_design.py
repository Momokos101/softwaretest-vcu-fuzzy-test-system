"""
VCU Module A — Data-driven test suites executed DIRECTLY from the reviewed
AutoTestDesign cases (docs/test_evidence/09_test_cases_reviewed.json).

This file is the executable realization of the full design pipeline:
    requirements → coverage items → strategy → 96 test cases
    (tool-generated by AutoTestDesign + human-reviewed REV-001..015) → THIS execution.

Every pytest parameter == exactly ONE design Test Case, so the suite is, by
construction, "developed based on the detailed test cases" (Assignment 2 §4).
Each parameter id encodes full traceability:  TC-ID | Coverage-Item | REQ | technique.

IEEE 829 Test Procedure lifecycle (Chp4 TestTech Part1, p19):
    setup    : fresh VCUSimulator per case          (setup_method)
    start    : adapt design in_data → VCU interface, drive the simulator
    measure  : assert each design expected_results oracle (eq/ne/gte/lte/gt/lt/contains)
    teardown : sim.reset()                          (teardown_method)

Risk-based execution order (Chp4 p17 + Chap5 Master Test Plan "Risk-Based Focus"):
    within each technique suite the cases are sorted by RPN ascending
    (RPN=1 == highest risk runs first; REQ-012 stuck defect, RPN=1, leads).

The ADAPTER (`_run`) bridges the gap between the LLM-authored abstract in_data
field names (voltage, h1, can_bus_idle, consecutive_wake_sleep_cycles, ...) and
the concrete VCUSimulator.simulate() signal interface. For single necessary-
condition sleep requirements (REQ-008/009/010) the adapter satisfies the OTHER
two h-conditions so the test isolates the condition under test (matching the
reviewed oracle vehicle_state eq 9 / ne 9).
"""
import json
import sys
import time
from pathlib import Path

import pytest

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "vcu_simulator"))

from simulator import VCUSimulator  # noqa: E402

_CASES = json.loads((ROOT / "docs/test_evidence/09_test_cases_reviewed.json").read_text(encoding="utf-8"))
_RISK = json.loads((ROOT / "docs/test_evidence/_state_snapshot/risk_analysis.json").read_text(encoding="utf-8"))


def _rpn(req: str) -> int:
    return (_RISK.get(req) or {}).get("rpn", 99)


def _idata(case: dict) -> dict:
    return {i["name"]: i.get("value") for i in case.get("in_data", [])}


def _to_int(v, default=0):
    try:
        if isinstance(v, str) and v.lower().startswith("0x"):
            return int(v, 16)
        return int(float(v))
    except (TypeError, ValueError):
        return default


def _match(actual, op: str, expected) -> bool:
    """Mirror of the tool's simulator_client._matches oracle comparison."""
    if op == "eq":
        return actual == expected or str(actual) == str(expected)
    if op == "ne":
        return not (actual == expected or str(actual) == str(expected))
    if op == "contains":
        if isinstance(actual, list):
            return any(expected == x or (isinstance(x, dict) and expected in x.values()) for x in actual)
        if isinstance(actual, (str, dict)):
            return expected in actual
        return False
    try:
        a, e = float(actual), float(expected)
    except (TypeError, ValueError):
        return False
    return {"gte": a >= e, "lte": a <= e, "gt": a > e, "lt": a < e}.get(op, False)


def _run(sim: VCUSimulator, case: dict) -> dict:
    """Adapter: translate a design case's in_data into VCUSimulator call(s)."""
    req = case["requirement_id"]
    d = _idata(case)

    if req == "REQ-001":
        return sim.simulate(supply_voltage=float(d.get("voltage")), duration_ms=_to_int(d.get("duration_ms"), 15))
    if req == "REQ-002":
        return sim.simulate(can_msg_id=_to_int(d.get("can_msg_id")))
    if req == "REQ-003":
        return sim.simulate(cp_voltage=float(d.get("cp_voltage")))
    if req == "REQ-004":
        return sim.simulate(cc_voltage=float(d.get("cc_voltage")))
    if req == "REQ-005":
        return sim.simulate(cc2_voltage=float(d.get("cc2_voltage")))
    if req == "REQ-006":
        return sim.simulate(hood_voltage=float(d.get("hood_voltage")), duration_ms=_to_int(d.get("duration_ms"), 15))
    if req == "REQ-007":
        return sim.simulate(door_voltage=float(d.get("door_voltage")), duration_ms=_to_int(d.get("duration_ms"), 15))
    # REQ-008/009/010/011: sleep-decision tests.
    # Precondition: VCU must be AWAKE (state11) first, so that "conditions not all
    # met" correctly MAINTAINS the wake state (vehicle_state ne 9), and "all met"
    # transitions to sleep (vehicle_state eq 9). REQ-008/009/010 isolate one
    # necessary condition by satisfying the other two.
    if req in ("REQ-008", "REQ-009", "REQ-010", "REQ-011"):
        sim.simulate(supply_voltage=9.3, duration_ms=15)  # wake → state11 precondition
        if req == "REQ-008":
            return sim.simulate(VCUO_bDIAG_VCUIdle_flg=_to_int(d.get("VCUO_bDIAG_VCUIdle_flg")),
                                VCUO_bDIAG_AuthComplete_flg=1, can_stopped=True)
        if req == "REQ-009":
            return sim.simulate(VCUO_bDIAG_VCUIdle_flg=1,
                                VCUO_bDIAG_AuthComplete_flg=_to_int(d.get("VCUO_bDIAG_AuthComplete_flg")),
                                can_stopped=True)
        if req == "REQ-010":
            return sim.simulate(VCUO_bDIAG_VCUIdle_flg=1, VCUO_bDIAG_AuthComplete_flg=1,
                                can_stopped=(_to_int(d.get("can_bus_idle")) == 1))
        return sim.simulate(VCUO_bDIAG_VCUIdle_flg=_to_int(d.get("h1")),
                            VCUO_bDIAG_AuthComplete_flg=_to_int(d.get("h2")),
                            can_stopped=(_to_int(d.get("h3")) == 1))
    # REQ-012: stuck reproduction is a SEQUENCE, not a single call.
    if req == "REQ-012":
        cycles = _to_int(d.get("consecutive_wake_sleep_cycles"), 3)
        interval = float(d.get("interval", 0.9) or 0.9)
        sim.reset()
        for i in range(cycles):
            if i > 0:
                time.sleep(interval)
            sim.simulate(supply_voltage=9.3, duration_ms=15)
            sim.simulate(VCUO_bDIAG_VCUIdle_flg=1, VCUO_bDIAG_AuthComplete_flg=1, can_stopped=True)
        return sim.simulate(supply_voltage=9.3, duration_ms=15)
    # REQ-013: drive VCU to the target observable state, then check output fields.
    if req == "REQ-013":
        target = _to_int(d.get("vehicle_state"), 11)
        if target == 9:
            return sim.simulate(VCUO_bDIAG_VCUIdle_flg=1, VCUO_bDIAG_AuthComplete_flg=1, can_stopped=True)
        return sim.simulate(supply_voltage=9.3, duration_ms=15)
    # REQ-014: timing. Compliant cases run a nominal op (type1≈14.7s, type2≈42s).
    # A timing-VIOLATION case (oracle asserts actual_duration gt threshold) is only
    # reachable via the stuck defect (type1 → 41s > 20s), so run the stuck sequence.
    if req == "REQ-014":
        wants_exceed = any(e.get("name") == "actual_duration" and e.get("operator") == "gt"
                           for e in case.get("expected_results", []))
        if wants_exceed:
            sim.reset()
            for i in range(3):
                if i > 0:
                    time.sleep(0.9)
                sim.simulate(supply_voltage=9.3, duration_ms=15)
                sim.simulate(VCUO_bDIAG_VCUIdle_flg=1, VCUO_bDIAG_AuthComplete_flg=1, can_stopped=True)
            return sim.simulate(supply_voltage=9.3, duration_ms=15)
        if _to_int(d.get("test_case_type"), 1) == 2:
            return sim.simulate(VCUO_bDIAG_VCUIdle_flg=1, VCUO_bDIAG_AuthComplete_flg=1, can_stopped=True)
        return sim.simulate(supply_voltage=9.3, duration_ms=15)
    raise AssertionError(f"No adapter mapping for {req}")


# Per-case execution detail, dumped after the session so the backend/UI can show
# exactly what the VCU returned for each test case.
_RESULTS: list = []


@pytest.fixture(scope="session", autouse=True)
def _dump_execution_details():
    yield
    out = ROOT / "docs" / "test_evidence" / "pytest_output" / "execution_details.json"
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(json.dumps(_RESULTS, ensure_ascii=False, indent=2, default=str), encoding="utf-8")


def _check_oracle(resp: dict, case: dict):
    """Measure step: assert every design expected_results entry against VCU output,
    and record the full VCU response + per-oracle outcome for later display."""
    failures = []
    for e in case.get("expected_results", []):
        name, op, val = e.get("name"), e.get("operator", "eq"), e.get("value")
        actual = resp.get(name)
        if not _match(actual, op, val):
            failures.append(f"{name} {op} {val!r} (actual={actual!r})")
    _RESULTS.append({
        "case_id": case["id"],
        "requirement_id": case["requirement_id"],
        "technique": case["technique"],
        "title": case.get("title", ""),
        "input": {i["name"]: i.get("value") for i in case.get("in_data", [])},
        "expected": [
            {"name": e.get("name"), "operator": e.get("operator", "eq"), "value": e.get("value")}
            for e in case.get("expected_results", [])
        ],
        "actual_output": resp,
        "passed": not failures,
        "mismatches": failures,
    })
    assert not failures, "oracle mismatch: " + "; ".join(failures)


def _params(technique):
    cases = [c for c in _CASES if c["technique"] == technique]
    cases.sort(key=lambda c: (_rpn(c["requirement_id"]), c["requirement_id"]))
    return [
        pytest.param(
            c,
            # NOTE: the case uuid is placed FIRST so the backend pytest_runner can
            # parse it out of the JUnit XML testcase name to map results back to
            # each tool test case (mark_execution). The rest is human-readable
            # traceability: REQ | RPN | title.
            id=f"{c['id']}|{c['requirement_id']}|RPN{_rpn(c['requirement_id'])}|{c.get('title','')[:10]}",
        )
        for c in cases
    ]


class _DesignSuiteBase:
    """IEEE 829 lifecycle shared by all technique suites (pytest-style hooks)."""

    def setup_method(self, _m):
        self.sim = VCUSimulator()
        self.sim.reset()

    def teardown_method(self, _m):
        self.sim.reset()


class TestSuiteEP_EquivalencePartitioning(_DesignSuiteBase):
    @pytest.mark.parametrize("case", _params("EP"))
    def test_ep(self, case):
        _check_oracle(_run(self.sim, case), case)


